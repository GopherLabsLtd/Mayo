package it.gopher.mayo.kotlin.enums

/**
 * Enum that contains all the Supported Http Method types.
 * <p>
 * The following method types are supported.
 * <p>
 *     1. GET
 *     2. POST
 *     3. PUT
 *     3. DELETE
 * @author Alex Nguyen
 */
enum class Method {
    GET,
    POST,
    PUT,
    DELETE
}